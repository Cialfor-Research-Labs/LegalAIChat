def _sqlite_table_exists(conn: sqlite3.Connection, table_name: str) -> bool:
    row = conn.execute(
        "SELECT name FROM sqlite_master WHERE type = 'table' AND name = ?",
        (table_name,),
    ).fetchone()
    return row is not None


def _sqlite_rows(conn: sqlite3.Connection, table_name: str) -> List[sqlite3.Row]:
    if not _sqlite_table_exists(conn, table_name):
        return []
    return conn.execute(f"SELECT * FROM {table_name}").fetchall()


def _coerce_bool(value: Any) -> bool:
    return bool(value)


def _executemany(conn: Any, sql: str, rows: Sequence[Sequence[Any]]) -> None:
    if not rows:
        return
    with conn.cursor() as cur:
        cur.executemany(sql, rows)


def _reset_identity_sequences(conn: Any) -> None:
    for table in ("users", "access_requests", "password_setup_tokens", "sessions", "chat_messages"):
        conn.execute(
            f"""
            SELECT setval(
                pg_get_serial_sequence('{table}', 'id'),
                COALESCE((SELECT MAX(id) FROM {table}), 1),
                (SELECT COUNT(*) > 0 FROM {table})
            )
            """
        )


def migrate(sqlite_path: str, truncate_first: bool) -> None:
    if not os.path.exists(sqlite_path):
        raise FileNotFoundError(f"SQLite file not found: {sqlite_path}")

    dotenv = _load_dotenv()
    sqlite_conn = sqlite3.connect(sqlite_path)
    sqlite_conn.row_factory = sqlite3.Row

    with _pg_connect(dotenv) as pg_conn:
        for statement in AUTH_SCHEMA_STATEMENTS:
            pg_conn.execute(statement)

        if truncate_first:
            pg_conn.execute(
                """
                TRUNCATE TABLE
                    chat_messages,
                    chat_sessions,
                    sessions,
                    password_setup_tokens,
                    access_requests,
                    users
                RESTART IDENTITY CASCADE
                """
            )

        users = _sqlite_rows(sqlite_conn, "users")
        _executemany(
            pg_conn,
            """
            INSERT INTO users
            (id, name, email, organization, use_case, advocate_address, advocate_mobile, role, status, access_granted, password_hash, created_at, updated_at)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    int(row["id"]),
                    row["name"],
                    row["email"],
                    row["organization"] or "",
                    row["use_case"] or "",
                    row["advocate_address"] if "advocate_address" in row.keys() else "",
                    row["advocate_mobile"] if "advocate_mobile" in row.keys() else "",
                    row["role"],
                    row["status"],
                    _coerce_bool(row["access_granted"]),
                    row["password_hash"],
                    row["created_at"],
                    row["updated_at"],
                )
                for row in users
            ],
        )

        access_requests = _sqlite_rows(sqlite_conn, "access_requests")
        _executemany(
            pg_conn,
            """
            INSERT INTO access_requests
            (id, user_id, name, email, organization, use_case, status, reviewed_by, review_notes, created_at, reviewed_at)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    int(row["id"]),
                    int(row["user_id"]),
                    row["name"],
                    row["email"],
                    row["organization"] or "",
                    row["use_case"] or "",
                    row["status"],
                    int(row["reviewed_by"]) if row["reviewed_by"] is not None else None,
                    row["review_notes"] or "",
                    row["created_at"],
                    row["reviewed_at"],
                )
                for row in access_requests
            ],
        )

        password_setup_tokens = _sqlite_rows(sqlite_conn, "password_setup_tokens")
        _executemany(
            pg_conn,
            """
            INSERT INTO password_setup_tokens
            (id, user_id, token_hash, expires_at, used, created_by_admin_id, created_at, used_at)
            VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    int(row["id"]),
                    int(row["user_id"]),
                    row["token_hash"],
                    row["expires_at"],
                    _coerce_bool(row["used"]),
                    int(row["created_by_admin_id"]) if row["created_by_admin_id"] is not None else None,
                    row["created_at"],
                    row["used_at"],
                )
                for row in password_setup_tokens
            ],
        )

        sessions = _sqlite_rows(sqlite_conn, "sessions")
        _executemany(
            pg_conn,
            """
            INSERT INTO sessions
            (id, user_id, token_hash, expires_at, revoked, created_at, revoked_at)
            VALUES (%s, %s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    int(row["id"]),
                    int(row["user_id"]),
                    row["token_hash"],
                    row["expires_at"],
                    _coerce_bool(row["revoked"]),
                    row["created_at"],
                    row["revoked_at"],
                )
                for row in sessions
            ],
        )

        chat_sessions = _sqlite_rows(sqlite_conn, "chat_sessions")
        _executemany(
            pg_conn,
            """
            INSERT INTO chat_sessions
            (session_id, user_id, title, created_at, updated_at, last_message_at, archived)
            VALUES (%s, %s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    row["session_id"],
                    int(row["user_id"]),
                    row["title"],
                    row["created_at"],
                    row["updated_at"],
                    row["last_message_at"],
                    _coerce_bool(row["archived"]),
                )
                for row in chat_sessions
            ],
        )

        chat_messages = _sqlite_rows(sqlite_conn, "chat_messages")
        _executemany(
            pg_conn,
            """
            INSERT INTO chat_messages
            (id, session_id, user_id, role, content, created_at)
            VALUES (%s, %s, %s, %s, %s, %s)
            """,
            [
                (
                    int(row["id"]),
                    row["session_id"],
                    int(row["user_id"]),
                    row["role"],
                    row["content"],
                    row["created_at"],
                )
                for row in chat_messages
            ],
        )

        _reset_identity_sequences(pg_conn)
        pg_conn.commit()

    sqlite_conn.close()

    print(f"Migrated users={len(users)}")
    print(f"Migrated access_requests={len(access_requests)}")
    print(f"Migrated password_setup_tokens={len(password_setup_tokens)}")
    print(f"Migrated sessions={len(sessions)}")
    print(f"Migrated chat_sessions={len(chat_sessions)}")
    print(f"Migrated chat_messages={len(chat_messages)}")


def main() -> None:
    parser = argparse.ArgumentParser(description="Migrate auth_access.db from SQLite to PostgreSQL.")
    parser.add_argument(
        "--sqlite-path",
        default=os.getenv("SQLITE_AUTH_DB_PATH", "auth_access.db"),
        help="Path to the SQLite auth DB file.",
    )
    parser.add_argument(
        "--skip-truncate",
        action="store_true",
        help="Do not clear PostgreSQL auth tables before loading data.",
    )
    args = parser.parse_args()
    migrate(sqlite_path=args.sqlite_path, truncate_first=not args.skip_truncate)


if __name__ == "__main__":
    main()
