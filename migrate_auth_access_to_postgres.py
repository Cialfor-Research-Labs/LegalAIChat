import argparse
import os
import sqlite3
from typing import Any, Dict, Iterable, List, Sequence, Tuple

import psycopg


def _load_dotenv(path: str = ".env") -> Dict[str, str]:
    parsed: Dict[str, str] = {}
    if not os.path.exists(path):
        return parsed
    with open(path, "r", encoding="utf-8") as fh:
        for raw_line in fh:
            line = raw_line.strip()
            if not line or line.startswith("#") or "=" not in line:
                continue
            key, value = line.split("=", 1)
            parsed[key.strip()] = value.strip()
    return parsed


def _env(dotenv: Dict[str, str], key: str, default: str = "") -> str:
    value = os.getenv(key)
    if value is not None:
        return value
    return dotenv.get(key, default)


def _pg_connect(dotenv: Dict[str, str]) -> Any:
    dsn = _env(dotenv, "AUTH_DB_DSN").strip()
    if dsn:
        return psycopg.connect(dsn)

    host = _env(dotenv, "AUTH_DB_HOST").strip()
    port = _env(dotenv, "AUTH_DB_PORT", "5432").strip()
    dbname = _env(dotenv, "AUTH_DB_NAME").strip()
    user = _env(dotenv, "AUTH_DB_USER").strip()
    password = _env(dotenv, "AUTH_DB_PASSWORD").strip()
    sslmode = _env(dotenv, "AUTH_DB_SSLMODE", "prefer").strip()

    missing = [
        name
        for name, value in (
            ("AUTH_DB_HOST", host),
            ("AUTH_DB_NAME", dbname),
            ("AUTH_DB_USER", user),
            ("AUTH_DB_PASSWORD", password),
        )
        if not value
    ]
    if missing:
        raise RuntimeError(
            "Missing PostgreSQL auth DB config. Set AUTH_DB_DSN or "
            + ", ".join(missing)
            + "."
        )

    return psycopg.connect(
        host=host,
        port=int(port),
        dbname=dbname,
        user=user,
        password=password,
        sslmode=sslmode,
    )


